/*创建账户表*/
CREATE TABLE FX_ACCOUNT(
  ACCOUNT_ID VARCHAR(32) PRIMARY KEY ,
  ACCOUNT_NAME VARCHAR(50) NOT NULL ,
  ACCOUNT_PASSWORD VARCHAR(100) NOT NULL ,
  BANKCARD_ID VARCHAR(19),
  FOREIGN KEY (BANKCARD_ID) REFERENCES FX_BANKCARD(BANKCARD_ID)
);
/*添加唯一约束*/
ALTER TABLE FX_ACCOUNT ADD UNIQUE (ACCOUNT_NAME);
ALTER TABLE FX_ACCOUNT ADD UNIQUE (BANKCARD_ID);


/*创建角色表*/
CREATE TABLE FX_ROLE(
  ROLE_ID VARCHAR(32) PRIMARY KEY ,
  ROLE_NAME VARCHAR(50) NOT NULL ,
  ROLE_DESCRIBE VARCHAR(200)
);
/*添加唯一约束*/
ALTER TABLE FX_ROLE ADD UNIQUE (ROLE_NAME);


/*创建账户角色映射表*/
CREATE TABLE FX_ACCOUNT_ROLE(
  ACCOUNT_ROLE_ID VARCHAR (32) PRIMARY KEY ,
  ACCOUNT_NAME VARCHAR (50) NOT NULL ,
  ROLE_NAME VARCHAR (50) NOT NULL ,
  CREATED_TIME TIMESTAMP NOT NULL ,
  FOREIGN KEY (ACCOUNT_NAME) REFERENCES FX_ACCOUNT(ACCOUNT_NAME) ,
  FOREIGN KEY (ROLE_NAME) REFERENCES FX_ROLE(ROLE_NAME)
);
